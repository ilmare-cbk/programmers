package Q31910;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.StringTokenizer;

/**
 * 이진수 격자
 */
public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int N = Integer.parseInt(br.readLine());
        boolean[][] arr = new boolean[N + 1][N + 1];

        for (int i = 1; i <= N; i++) {
            StringTokenizer st = new StringTokenizer(br.readLine());
            for (int j = 1; j <= N; j++) {
                arr[i][j] = Integer.parseInt(st.nextToken()) == 1;
            }
        }

        String[][] dp = new String[N + 1][N + 1];
        for (int i = 0; i <= N; i++) {
            dp[i][0] = "0";
            dp[0][i] = "0";
        }

        for (int i = 1; i <= N; i++) {
            for (int j = 1; j <= N; j++) {
                if (i == 1 && j == 1) dp[i][j] = String.valueOf(arr[i][j] ? 1 : 0);
                else {
                    if (Long.valueOf(dp[i - 1][j], 2) > Long.valueOf(dp[i][j - 1], 2)) {
                        dp[i][j] = dp[i - 1][j] + (arr[i][j] ? 1 : 0);
                    } else {
                        dp[i][j] = dp[i][j - 1] + (arr[i][j] ? 1 : 0);
                    }
                }
            }
        }

        System.out.println(Long.valueOf(dp[N][N], 2));

    }
}
